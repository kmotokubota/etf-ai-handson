-- ============================================================================
-- ETF AI Hands-on: Setup Script
-- ============================================================================
-- 対象: ポートフォリオマネージャー向けハンズオン
-- 概要: ETFポートフォリオ分析AIアシスタントの構築に必要なオブジェクトを作成します
--
-- 作成されるオブジェクト:
--   Database  : ETF_AI_HANDSON_DB
--   Schemas   : DEMO_SCHEMA, AI
--   Warehouses: DEMO_WH, COMPUTE_WH
--   Tables    : DIM_ETF, DIM_PORTFOLIO, FACT_HOLDINGS, FACT_PERFORMANCE,
--               MARKET_NEWS, ETF_DESCRIPTIONS
--   Integration: git_api_integration (GitHub)
--   Git Repo  : ETF_AI_HANDSON_DB.AI.ETF_AI_HANDSON_REPO
--   Stages    : PROSPECTUS_STAGE (目論見書PDF), DOCUMENTS_STAGE (月次レポートPNG)
-- ============================================================================

USE ROLE ACCOUNTADMIN;

ALTER SESSION SET query_tag = 'etf_ai_handson_setup';

-- ============================================================================
-- Section 1: データベース・スキーマ作成
-- ============================================================================

CREATE DATABASE IF NOT EXISTS ETF_AI_HANDSON_DB
    COMMENT = 'ETF AI Hands-on - ポートフォリオマネージャー向けデモ';

CREATE SCHEMA IF NOT EXISTS ETF_AI_HANDSON_DB.DEMO_SCHEMA
    COMMENT = 'デモ用データスキーマ（テーブル・ビュー）';

CREATE SCHEMA IF NOT EXISTS ETF_AI_HANDSON_DB.AI
    COMMENT = 'AI コンポーネント（Semantic View、Cortex Search、Agent）';

-- ============================================================================
-- Section 2: ウェアハウス作成
-- ============================================================================

CREATE WAREHOUSE IF NOT EXISTS DEMO_WH
    WAREHOUSE_SIZE = 'XSMALL'
    AUTO_SUSPEND = 120
    AUTO_RESUME = TRUE
    INITIALLY_SUSPENDED = TRUE
    COMMENT = 'AI Functions・Cortex Analyst 用ウェアハウス';

CREATE WAREHOUSE IF NOT EXISTS COMPUTE_WH
    WAREHOUSE_SIZE = 'XSMALL'
    AUTO_SUSPEND = 120
    AUTO_RESUME = TRUE
    INITIALLY_SUSPENDED = TRUE
    COMMENT = 'Cortex Search インデックス構築用ウェアハウス';

-- ============================================================================
-- Section 3: Cortex AI 設定
-- ============================================================================

ALTER ACCOUNT SET CORTEX_ENABLED_CROSS_REGION = 'ANY_REGION';

-- ============================================================================
-- Section 4: テーブル作成
-- ============================================================================

USE DATABASE ETF_AI_HANDSON_DB;
USE SCHEMA DEMO_SCHEMA;
USE WAREHOUSE DEMO_WH;

-- ETF銘柄マスタ
CREATE OR REPLACE TABLE DIM_ETF (
    ETF_CODE        VARCHAR(10)     NOT NULL COMMENT 'ETFコード（証券コード）',
    ETF_NAME        VARCHAR(200)    NOT NULL COMMENT 'ETF正式名称',
    ETF_NAME_SHORT  VARCHAR(100)    COMMENT 'ETF略称',
    CATEGORY        VARCHAR(50)     COMMENT 'カテゴリ（コア/成長テーマ/インカム/コモディティ）',
    ASSET_CLASS     VARCHAR(50)     COMMENT '資産クラス（日本株式/米国株式/債券/REIT）',
    THEME           VARCHAR(100)    COMMENT '投資テーマ',
    AUM_BILLION_JPY DECIMAL(10,2)   COMMENT '純資産総額（億円）',
    NAV             DECIMAL(12,0)   COMMENT '基準価額（円/100口）',
    EXPENSE_RATIO   DECIMAL(6,4)    COMMENT '実質コスト（年率）',
    NISA_ELIGIBLE   BOOLEAN         DEFAULT TRUE COMMENT 'NISA成長投資枠対象',
    INCEPTION_DATE  DATE            COMMENT '設定日',
    BENCHMARK       VARCHAR(200)    COMMENT 'ベンチマーク指数',
    DESCRIPTION     VARCHAR(1000)   COMMENT 'ファンド説明'
);

-- ポートフォリオマスタ
CREATE OR REPLACE TABLE DIM_PORTFOLIO (
    PORTFOLIO_ID    VARCHAR(10)     NOT NULL COMMENT 'ポートフォリオID',
    PORTFOLIO_NAME  VARCHAR(100)    NOT NULL COMMENT 'ポートフォリオ名称',
    MANAGER_NAME    VARCHAR(50)     COMMENT 'ポートフォリオマネージャー名',
    OBJECTIVE       VARCHAR(200)    COMMENT '運用目標',
    RISK_PROFILE    VARCHAR(50)     COMMENT 'リスクプロファイル（積極/バランス/安定）',
    TARGET_RETURN   DECIMAL(5,2)    COMMENT '目標リターン（年率%）',
    AUM_MILLION_JPY DECIMAL(15,2)   COMMENT '運用資産額（百万円）',
    INCEPTION_DATE  DATE            COMMENT '設定日',
    CURRENCY        VARCHAR(10)     DEFAULT 'JPY' COMMENT '通貨'
);

-- 保有ETF明細
CREATE OR REPLACE TABLE FACT_HOLDINGS (
    HOLDING_ID          VARCHAR(20)     NOT NULL COMMENT '保有ID',
    PORTFOLIO_ID        VARCHAR(10)     NOT NULL COMMENT 'ポートフォリオID',
    ETF_CODE            VARCHAR(10)     NOT NULL COMMENT 'ETFコード',
    HOLDING_DATE        DATE            NOT NULL COMMENT '基準日',
    UNITS_HELD          DECIMAL(18,0)   COMMENT '保有口数',
    MARKET_VALUE_JPY    DECIMAL(20,2)   COMMENT '時価評価額（円）',
    WEIGHT_PCT          DECIMAL(6,3)    COMMENT '比重（%）',
    UNREALIZED_GAIN_JPY DECIMAL(20,2)   COMMENT '含み損益（円）',
    UNREALIZED_GAIN_PCT DECIMAL(6,2)    COMMENT '含み損益率（%）'
);

-- パフォーマンス実績（月次）
CREATE OR REPLACE TABLE FACT_PERFORMANCE (
    PERFORMANCE_ID  VARCHAR(20)     NOT NULL COMMENT 'パフォーマンスID',
    PORTFOLIO_ID    VARCHAR(10)     NOT NULL COMMENT 'ポートフォリオID',
    ETF_CODE        VARCHAR(10)     COMMENT 'ETFコード（NULLの場合はポートフォリオ全体）',
    PERF_DATE       DATE            NOT NULL COMMENT '評価日',
    RETURN_1M_PCT   DECIMAL(7,3)    COMMENT '1ヶ月リターン（%）',
    RETURN_3M_PCT   DECIMAL(7,3)    COMMENT '3ヶ月リターン（%）',
    RETURN_YTD_PCT  DECIMAL(7,3)    COMMENT '年初来リターン（%）',
    RETURN_1Y_PCT   DECIMAL(7,3)    COMMENT '1年リターン（%）',
    VOLATILITY_1Y   DECIMAL(7,3)    COMMENT '1年ボラティリティ（%）',
    SHARPE_RATIO    DECIMAL(7,3)    COMMENT 'シャープレシオ',
    MAX_DRAWDOWN    DECIMAL(7,3)    COMMENT '最大ドローダウン（%）',
    BENCHMARK_RETURN_1M DECIMAL(7,3) COMMENT 'ベンチマーク1ヶ月リターン（%）'
);

-- マーケットニュース
CREATE OR REPLACE TABLE MARKET_NEWS (
    NEWS_ID         VARCHAR(10)     NOT NULL COMMENT 'ニュースID',
    NEWS_DATE       DATE            NOT NULL COMMENT '配信日',
    HEADLINE        VARCHAR(300)    NOT NULL COMMENT 'ヘッドライン',
    BODY            VARCHAR(2000)   COMMENT '本文',
    SOURCE          VARCHAR(100)    COMMENT '情報源',
    CATEGORY        VARCHAR(50)     COMMENT 'カテゴリ（市場動向/個別ETF/規制/経済指標）',
    RELEVANT_ETFS   VARCHAR(200)    COMMENT '関連ETFコード',
    SENTIMENT       VARCHAR(20)     COMMENT '（AI分析前は空白）'
);

-- ETFファンド説明文書（Cortex Search ソース）
CREATE OR REPLACE TABLE ETF_DESCRIPTIONS (
    DOC_ID          VARCHAR(20)     NOT NULL COMMENT '文書ID',
    ETF_CODE        VARCHAR(10)     NOT NULL COMMENT 'ETFコード',
    ETF_NAME        VARCHAR(200)    COMMENT 'ETF名称',
    SECTION         VARCHAR(100)    COMMENT 'セクション名',
    CONTENT         VARCHAR(4000)   COMMENT '本文テキスト',
    DOC_TYPE        VARCHAR(50)     COMMENT '文書種別（ファクトシート/目論見書/運用レポート）',
    PUBLISHED_DATE  DATE            COMMENT '公開日'
);

